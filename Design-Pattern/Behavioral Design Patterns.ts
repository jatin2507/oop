`Behavioral patterns control communication and the assignment of responsibilities between different objects.`;

`The most common behavioral patterns are:`;
`Chain of Responsibility Pattern`;
`Command Pattern`;
`Iterator Pattern`;
`Mediator Pattern`;
`Memento Pattern`;
`Observer Pattern`;
`State Pattern`;
`Strategy Pattern`;
`Template Method Pattern`;
`Visitor Pattern`;

`Chain of Responsibility Pattern`;
`The Chain of Responsibility pattern is used to process varied requests, each of which may be dealt with by a different handler.`;
`The Chain of Responsibility pattern provides a chain of loosely coupled objects one of which can satisfy a request.`;

`Command Pattern`;
`The Command pattern encapsulates a request as an object, thereby letting you parameterize other objects with different requests, queue or log requests, and support undoable operations.`;

`Iterator Pattern`;
`The Iterator pattern provides a way to access the elements of an aggregate object sequentially without exposing its underlying representation.`;

`Mediator Pattern`;
`The Mediator pattern defines an object that encapsulates how a set of objects interact. This pattern is considered to be a behavioral pattern due to the way it can alter the program's running behavior.`;

`Memento Pattern`;
`The Memento pattern provides the ability to restore an object to its previous state (undo).`;

`Observer Pattern`;
`The Observer pattern is used when the change of one object needs to be reflected in another object without keeping the objects tight coupled.`;

`State Pattern`;
`The State pattern provides a way to change the behavior of an object when its internal state changes.`;

`Strategy Pattern`;
`The Strategy pattern encapsulates alternative algorithms (or strategies) for a particular task. It allows a method to be swapped out at runtime by any other method (strategy) without the client realizing it.`;

`Template Method Pattern`;
`The Template Method pattern defines the skeleton of an algorithm in a method, deferring some steps to subclasses. Template Method lets subclasses redefine certain steps of an algorithm without changing the algorithm's structure.`;

`Visitor Pattern`;
`The Visitor pattern is used to separate a relatively complex set of structured data classes from the functionality that may be performed upon the data that they hold.`;

`Structural patterns are concerned with how classes and objects are composed to form larger structures.`;
